class Square extends RegularPolygon {


    public Square(double length) {
        super(4, length);
    }

    @Override
    public double perimeter() {
        return super.perimeter();
    }
}


class EquilateralTriangle extends RegularPolygon {


    public EquilateralTriangle(double length) {
        super(3, length);
    }

    @Override
    public double perimeter() {
        return super.perimeter();
    }
}
